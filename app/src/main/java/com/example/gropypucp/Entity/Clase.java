package com.example.gropypucp.Entity;

public class Clase {

    private String clase;
    private String proyecto;
    private String descripcion;
    private String numeroGrupos;
    private String numeroIntegrantes;
    private String tiempo;
    private String uidProfesor;

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }

    public String getProyecto() {
        return proyecto;
    }

    public void setProyecto(String proyecto) {
        this.proyecto = proyecto;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getNumeroGrupos() {
        return numeroGrupos;
    }

    public void setNumeroGrupos(String numeroGrupos) {
        this.numeroGrupos = numeroGrupos;
    }

    public String getNumeroIntegrantes() {
        return numeroIntegrantes;
    }

    public void setNumeroIntegrantes(String numeroIntegrantes) {
        this.numeroIntegrantes = numeroIntegrantes;
    }

    public String getTiempo() {
        return tiempo;
    }

    public void setTiempo(String tiempo) {
        this.tiempo = tiempo;
    }

    public String getUidProfesor() {
        return uidProfesor;
    }

    public void setUidProfesor(String uidProfesor) {
        this.uidProfesor = uidProfesor;
    }
}
